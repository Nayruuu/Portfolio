#!/usr/bin/env node
/**
 * GENERATES `src/app/core/content/article-bodies.ts` from the Markdown files in
 * `src/content/articles/<slug>.<lang>.md` — so adding a language or an article needs no hand-edit
 * (the same pattern as `icon-set.ts`). The `Record<Lang, string>` type makes a missing translation
 * a build error (completeness net). Run: `make gen-article-bodies` (and after `make i18n`).
 */
import { readdirSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const APP_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const ARTICLES_DIR = resolve(APP_ROOT, 'src/content/articles');
const OUT = resolve(APP_ROOT, 'src/app/core/content/article-bodies.ts');

const camel = (slug) => slug.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
const cap = (lang) => lang[0].toUpperCase() + lang.slice(1);

/** slug → { lang → import-var name } */
const bySlug = {};
for (const file of readdirSync(ARTICLES_DIR).filter((f) => f.endsWith('.md')).sort()) {
  const match = file.match(/^(.+)\.([a-z]{2})\.md$/);

  if (!match) {
    continue;
  }
  const [, slug, lang] = match;

  (bySlug[slug] ??= {})[lang] = `${camel(slug)}${cap(lang)}`;
}

const slugs = Object.keys(bySlug).sort();
const imports = slugs
  .flatMap((slug) =>
    Object.entries(bySlug[slug]).map(
      ([lang, varName]) => `import ${varName} from '../../../content/articles/${slug}.${lang}.md';`,
    ),
  )
  .join('\n');

const entries = slugs
  .map((slug) => {
    const langs = Object.entries(bySlug[slug])
      .map(([lang, varName]) => `${lang}: ${varName}`)
      .join(', ');

    return `  '${slug}': { ${langs} },`;
  })
  .join('\n');

writeFileSync(
  OUT,
  `// GENERATED by scripts/gen-article-bodies.mjs — do not edit. Run \`make gen-article-bodies\`.

import type { Lang } from '../../domain';

${imports}

/** slug → raw Markdown body per language. Single source for render + prerender. */
export const ARTICLE_BODIES: Record<string, Record<Lang, string>> = {
${entries}
};
`,
);
console.log(`✓ article-bodies.ts — ${slugs.length} articles`);
